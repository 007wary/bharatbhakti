export const getUpcomingFestivals = (days = 30) => {
  const today = new Date();
  today.setHours(0, 0, 0, 0);
  const future = new Date(today);
  future.setDate(future.getDate() + days);
  return FESTIVALS.filter(f => f.date >= today && f.date <= future)
    .sort((a, b) => a.date - b.date);
};

export const getTodayFestival = () => {
  const today = new Date().toDateString();
  return FESTIVALS.find(f => f.date.toDateString() === today) || null;
};

export const getFestivalsByMonth = () => {
  const grouped = {};
  FESTIVALS.forEach(f => {
    const key = f.date.toLocaleDateString('en-IN', { month: 'long', year: 'numeric' });
    if (!grouped[key]) grouped[key] = [];
    grouped[key].push(f);
  });
  return grouped;
};

export const getDaysUntil = (date) => {
  const today = new Date();
  today.setHours(0, 0, 0, 0);
  const diff = Math.ceil((date - today) / (1000 * 60 * 60 * 24));
  if (diff === 0) return 'Today';
  if (diff === 1) return 'Tomorrow';
  if (diff < 0) return null;
  return `In ${diff} days`;
};